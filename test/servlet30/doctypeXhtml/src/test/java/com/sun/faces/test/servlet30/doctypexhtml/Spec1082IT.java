package com.sun.faces.test.servlet30.doctypexhtml;

import com.gargoylesoftware.htmlunit.WebClient;
import com.gargoylesoftware.htmlunit.html.HtmlPage;
import org.junit.After;
import org.junit.Before;
import org.junit.Test;
import static org.junit.Assert.*;

public class Spec1082IT {

    private String webUrl;
    private WebClient webClient;

    @Before
    public void setUp() {
        webUrl = System.getProperty("integration.url");
        webClient = new WebClient();
    }

    @After
    public void tearDown() {
        webClient.close();
    }

    @Test
    public void testDoctypeHtml5() throws Exception {
        HtmlPage page = webClient.getPage(webUrl + "faces/index.xhtml");
        assertEquals("html", page.getDoctype().getName());
        assertEquals("-//W3C//DTD XHTML Basic 1.0//EN", page.getDoctype().getPublicId());
        assertEquals("http://www.w3.org/TR/xhtml-basic/xhtml-basic10.dtd", page.getDoctype().getSystemId());
    }
}
